package school;

public class People {
	// 属性
	private String Name;
	private int No;
	private String sex;
	private int grand;
	private int age;

	// 成员方法
	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public int getNo() {
		return No;
	}

	public void setNo(int no) {
		No = no;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getGrand() {
		return grand;
	}

	public void setGrand(int grand) {
		this.grand = grand;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
